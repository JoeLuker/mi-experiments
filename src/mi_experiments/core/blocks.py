from typing import Optional

import mlx.core as mx
import mlx.nn as nn

from mi_experiments.core.attention import Attention
from mi_experiments.core.config import ModelArgs
from mi_experiments.core.mlp import MLP
from mi_experiments.core.cache import KVCache


class TransformerBlock(nn.Module):
    def __init__(self, args: ModelArgs):
        super().__init__()
        self.num_attention_heads = args.num_attention_heads
        self.hidden_size = args.hidden_size
        self.self_attn = Attention(args)
        self.mlp = MLP(args)
        self.input_layernorm = nn.RMSNorm(args.hidden_size, eps=args.rms_norm_eps)
        self.post_attention_layernorm = nn.RMSNorm(
            args.hidden_size, eps=args.rms_norm_eps
        )
        self.args = args
        # Emphasis scaling factor for the layer
        self.layer_scale = mx.array(1.0, dtype=mx.float32)  # Default scaling factor

    def __call__(
        self,
        x: mx.array,
        mask: Optional[mx.array] = None,
        cache: Optional[KVCache] = None,
    ) -> mx.array:
        r = self.self_attn(self.input_layernorm(x), mask, cache)
        h = x + r
        r = self.mlp(self.post_attention_layernorm(h))
        out = h + r
        return out * self.layer_scale  # Apply layer scaling
